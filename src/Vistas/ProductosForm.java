    void listar() {
        List<producto> lista = dao1.listar();
        modelo = (DefaultTableModel) tablaP.getModel();
        Object[] ob = new Object[5];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getId();
            ob[1] = lista.get(i).getNomP();
            ob[2] = lista.get(i).getPrecioP();
            ob[3] = lista.get(i).getStockP();
            ob[4] = lista.get(i).getEstadoP();
            modelo.addRow(ob);

        }
        tablaP.setModel(modelo);
    }
    
    
    
    
    
    
