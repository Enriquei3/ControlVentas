    private void tablaPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPMouseClicked

        int fila=tablaP.getSelectedRow();

        if(fila == -1){
            JOptionPane.showMessageDialog(this,"Debe Seleccionar una Fila");
        }else{
            id= Integer.parseInt(tablaP.getValueAt(fila, 0).toString());
            String nom=tablaP.getValueAt(fila, 1).toString();
            String precio=tablaP.getValueAt(fila, 2).toString();
            String Stock=tablaP.getValueAt(fila, 3).toString();
            String estado=tablaP.getValueAt(fila, 4).toString();
            txtnomP.setText(nom);
            txtprecioP.setText(precio);
            txtstockP.setText(Stock);
            comboEstadoP.setSelectedItem(estado);
        }

    }//GEN-LAST:event_tablaPMouseClicked

     void agregar() {
        String nomP = txtnomP.getText();
        String precioP = txtprecioP.getText();
        String stockP = txtstockP.getText();
        String esP = comboEstadoP.getSelectedItem().toString();

        Object[] ob1 = new Object[4];
        ob1[0] = nomP;
        ob1[1] = precioP;
        ob1[2] = stockP;
        ob1[3] = esP;
        dao1.add(ob1);

    }

    void actualizar() {
        int fila = tablaP.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar una fila");

        } else {

            String nomP = txtnomP.getText();
            String precioP = txtprecioP.getText();
            String stockP = txtstockP.getText();
            String esP = comboEstadoP.getSelectedItem().toString();

            Object[] obj1 = new Object[5];

            obj1[0] = nomP;
            obj1[1] = precioP;
            obj1[2] = stockP;
            obj1[3] = esP;
            obj1[4] = id;
            dao1.actualizar(obj1);

        }

    }

    void eliminar() {
        int fila = tablaP.getSelectedRow();
        int ids = Integer.parseInt(tablaP.getValueAt(fila, 0).toString());
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar una fila");

        } else {

            dao1.eliminar(ids);
        }

    }

    void nuevo() {
        txtnomP.setText("");
        txtprecioP.setText("");
        txtstockP.setText("");
        comboEstadoP.setSelectedItem(0);
        txtnomP.requestFocus();

    }

    void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;

        }
    }

    
    
    
    
    

